def printRed(skk, obj=''): print("\033[91m {}\033[00m" .format(skk), obj) 
def printGreen(skk, obj=''): print("\033[92m {}\033[00m" .format(skk), obj) 
def printYellow(skk, obj=''): print("\033[93m {}\033[00m" .format(skk), obj) 
def printLightPurple(skk, obj=''): print("\033[94m {}\033[00m" .format(skk), obj) 
def printPurple(skk, obj=''): print("\033[95m {}\033[00m" .format(skk), obj) 
def printCyan(skk, obj=''): print("\033[96m {}\033[00m" .format(skk), obj) 
def printLightGray(skk, obj=''): print("\033[97m {}\033[00m" .format(skk), obj) 
def printBlack(skk, obj=''): print("\033[98m {}\033[00m" .format(skk), obj) 
